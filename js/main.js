// Pink Bubble Hover
document.getElementById("pink").addEventListener("mouseenter",
  function() {
    document.getElementById("text").style.backgroundColor = "#EBBFD0"
    document.getElementById("text").innerHTML = "Lorem ipsum dolor sit amet, consectetur adipisicing elit, sed do eiusmod tempor incididunt ut labore et dolore magna aliqua. Ut enim ad minim veniam, quis nostrud exercitation ullamco laboris nisi ut aliquip ex ea commodo consequat. Duis aute irure dolor in reprehenderit in voluptate velit esse cillum dolore eu fugiat nulla pariatur. Excepteur sint occaecat cupidatat non proident, sunt in culpa qui officia deserunt mollit anim id est laborum."
    document.getElementById("text").style.textAlign = "left"
    document.getElementById("text").style.fontSize = "1rem";
    document.getElementById("text").style.color = "#F7F7F7"
  }, false
);

document.getElementById("pink").addEventListener("mouseleave",
  function() {
    document.getElementById("text").style.backgroundColor = "transparent";
    document.getElementById("text").innerHTML = "Hover over the services above to learn more."
    document.getElementById("text").style.textAlign = "center"
    document.getElementById("text").style.fontSize = "1.5rem";
    document.getElementById("text").style.color = "#22293D"
  }, false
);

